var tabela1 = document.getElementById("minhaTabela1");
var linhas1 = tabela1.getElementsByTagName("tr");

for(var i = 1; i < linhas1.length; i++){
    var linha = linhas1[i];

    linha.addEventListener("click", function(){
        //Adicionar ao atual
        selLinha(this, false); //Selecione apenas um
        //selLinha(this, true); //Selecione quantos quiser
    });
}

/**
 Caso passe true, você pode selecionar multiplas linhas.
 Caso passe false, você só pode selecionar uma linha por vez.
 **/
function selLinha(linha, multiplos){
    if(!multiplos){
        var linhas1 = linha.parentElement.getElementsByTagName("tr");
        for(var i = 0; i < linhas1.length; i++){
            var linha_ = linhas1[i];
            linha_.classList.remove("selecionado");
        }
    }
    linha.classList.toggle("selecionado");
}

