var tbody1 = document.getElementById("tbody1");

document.getElementById("txtBusca1").addEventListener("keyup",function () {
    var busca = document.getElementById("txtBusca1").value.toLowerCase();
    //console.log(busca);

    for(var i = 0; i < tbody1.childNodes.length; i++) {
        var achou = false;
        var tr = tbody1.childNodes[i];
        //console.log(tr);
        var td = tr.childNodes;
        //console.log(td);



        //
        for (var j = 0; j < td.length; j++) {
            //console.log(j);
            var value = td[j].childNodes[0].nodeValue.toLowerCase();


            //console.log(td[j].childNodes[0].nodeValue.toLowerCase());
            if (value.indexOf(busca) >= 0) {
                //console.log(value.indexOf(busca));
                achou = true;
            }
        }
        // console.log(achou);
        if(achou){
            tr.style.display = "table-row";
        }else{
            tr.style.display = "none";
        }
    }
});




















